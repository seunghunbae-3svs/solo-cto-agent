#!/usr/bin/env node

const fs = require('fs');
const path = require('path');
const os = require('os');

/**
 * Knowledge Article Generator
 * Synthesizes CI/CD patterns into reusable local knowledge articles.
 *
 * Articles stored in: ~/.claude/skills/solo-cto-agent/knowledge/
 * Triggered by: `solo-cto-agent learn`
 */

const KNOWLEDGE_DIR = path.join(
  os.homedir(),
  '.claude',
  'skills',
  'solo-cto-agent',
  'knowledge'
);

const DATA_DIR = path.join(
  os.homedir(),
  '.claude',
  'data',
  'solo-cto-agent'
);

/**
 * Ensure knowledge directory exists
 */
function ensureKnowledgeDir() {
  if (!fs.existsSync(KNOWLEDGE_DIR)) {
    fs.mkdirSync(KNOWLEDGE_DIR, { recursive: true });
  }
}

/**
 * Read and parse local data files
 */
function readDataFile(filename) {
  const filepath = path.join(DATA_DIR, filename);
  if (!fs.existsSync(filepath)) {
    return null;
  }
  try {
    return JSON.parse(fs.readFileSync(filepath, 'utf8'));
  } catch (e) {
    return null;
  }
}

/**
 * Scan failure catalog and group by category/tags
 */
function scanFailureCatalog() {
  const catalog = readDataFile('failure-catalog.json');
  if (!catalog || !catalog.errors) {
    return { patterns: [], count: 0 };
  }

  const patterns = catalog.errors.map(err => ({
    message: err.message,
    regex: err.regex,
    tags: err.tags || [],
    repo: err.repo,
    timestamp: err.timestamp,
    frequency: err.frequency || 1
  }));

  return { patterns, count: patterns.length };
}

/**
 * Scan agent scores and extract performance trends
 */
function scanAgentScores() {
  const scores = readDataFile('agent-scores-local.json');
  if (!scores || !scores.agents) {
    return { agents: {}, count: 0 };
  }

  const agents = {};
  const repos = new Set();

  scores.agents.forEach(agent => {
    const repo = agent.repo || 'unknown';
    repos.add(repo);

    if (!agents[agent.name]) {
      agents[agent.name] = {
        name: agent.name,
        repos: {},
        totalAccuracy: 0,
        reviewCount: 0
      };
    }

    if (!agents[agent.name].repos[repo]) {
      agents[agent.name].repos[repo] = {
        accuracy: agent.accuracy || 0,
        reviewCount: agent.reviewCount || 0
      };
    }

    agents[agent.name].totalAccuracy += agent.accuracy || 0;
    agents[agent.name].reviewCount += 1;
  });

  // Compute averages
  Object.values(agents).forEach(agent => {
    if (agent.reviewCount > 0) {
      agent.averageAccuracy = (agent.totalAccuracy / agent.reviewCount).toFixed(2);
    }
  });

  return { agents, repoCount: repos.size };
}

/**
 * Categorize patterns into buckets
 */
function categorizePatterns(patterns) {
  const categories = {
    deploy: [],
    database: [],
    auth: [],
    style: [],
    general: []
  };

  patterns.forEach(pattern => {
    const msg = (pattern.message || '').toLowerCase();
    const tags = (pattern.tags || []).map(t => t.toLowerCase());
    const combined = `${msg} ${tags.join(' ')}`;

    if (combined.match(/vercel|netlify|build|deployment|timeout|sigterm|exit code/)) {
      categories.deploy.push(pattern);
    } else if (combined.match(/prisma|migration|database|supabase|postgres|schema/)) {
      categories.database.push(pattern);
    } else if (combined.match(/auth|token|oauth|nextauth|jwt|session/)) {
      categories.auth.push(pattern);
    } else if (combined.match(/tailwind|css|style|class|ui|component/)) {
      categories.style.push(pattern);
    } else {
      categories.general.push(pattern);
    }
  });

  return categories;
}

/**
 * Generate a single knowledge article for a category
 */
function generateArticle(category, patterns) {
  if (patterns.length === 0) {
    return null;
  }

  const categoryLabels = {
    deploy: 'Deploy Failures',
    database: 'Database Patterns',
    auth: 'Authentication & Authorization',
    style: 'Styling & UI',
    general: 'General Issues'
  };

  const title = categoryLabels[category] || category;
  const now = new Date().toISOString().split('T')[0];

  let content = `# ${title} — Knowledge Article

> Auto-generated by solo-cto-agent learn
> Last updated: ${now}
> Based on: ${patterns.length} error pattern${patterns.length === 1 ? '' : 's'}, ${patterns.reduce((sum, p) => sum + (p.frequency || 1), 0)} total occurrences

## Common Issues

`;

  patterns.forEach((pattern, idx) => {
    const freq = pattern.frequency || 1;
    content += `### ${idx + 1}. ${pattern.message}
**Pattern:** \`${pattern.regex || 'N/A'}\`
**Tags:** ${pattern.tags && pattern.tags.length > 0 ? pattern.tags.join(', ') : 'general'}
**Frequency:** ${freq} occurrence${freq === 1 ? '' : 's'}

`;
  });

  content += `## Prevention Checklist
- [ ] Review recent error patterns before deployment
- [ ] Run local build and tests before pushing
- [ ] Check environment variables are properly configured
- [ ] Monitor logs after deployment

## Agent Notes
- These patterns are automatically tracked from CI/CD runs
- Use this article to help identify and prevent recurring issues
- Update this knowledge base regularly via \`solo-cto-agent learn\`
`;

  return {
    name: `${category}-patterns.md`,
    content,
    patternCount: patterns.length
  };
}

/**
 * Generate agent performance article
 */
function generateAgentArticle(agentData) {
  if (!agentData || Object.keys(agentData.agents).length === 0) {
    return null;
  }

  const now = new Date().toISOString().split('T')[0];
  let content = `# Agent Performance — Knowledge Article

> Auto-generated from agent-scores-local.json
> Last updated: ${now}

## Overall Performance

| Agent | Avg Accuracy | Reviews | Status |
|---|---|---|---|
`;

  Object.values(agentData.agents).forEach(agent => {
    content += `| ${agent.name} | ${agent.averageAccuracy || 'N/A'} | ${agent.reviewCount} | Active |
`;
  });

  content += `

## Per-Repo Breakdown

`;

  Object.entries(agentData.agents).forEach(([agentName, agent]) => {
    if (Object.keys(agent.repos).length > 0) {
      content += `### ${agentName}

| Repo | Accuracy | Reviews |
|---|---|---|
`;
      Object.entries(agent.repos).forEach(([repo, metrics]) => {
        content += `| ${repo} | ${(metrics.accuracy || 0).toFixed(2)} | ${metrics.reviewCount} |
`;
      });
      content += `
`;
    }
  });

  content += `## Trends & Recommendations
- Review these scores to understand agent strengths per repository
- Consider routing different task types to different agents
- Track accuracy improvements over time

## Next Steps
- Run \`solo-cto-agent sync\` to update agent scores
- Use \`solo-cto-agent learn\` to regenerate this article
`;

  return {
    name: 'agent-performance.md',
    content,
    patternCount: Object.keys(agentData.agents).length
  };
}

/**
 * Check if article needs regeneration (older than 7 days)
 */
function shouldRegenerate(filepath, force = false) {
  if (force) return true;
  if (!fs.existsSync(filepath)) return true;

  const stat = fs.statSync(filepath);
  const ageMs = Date.now() - stat.mtime.getTime();
  const ageDays = ageMs / (1000 * 60 * 60 * 24);

  return ageDays > 7;
}

/**
 * Write article to disk
 */
function writeArticle(article) {
  const filepath = path.join(KNOWLEDGE_DIR, article.name);
  fs.writeFileSync(filepath, article.content, 'utf8');
  return filepath;
}

/**
 * Main knowledge generation function
 */
async function generateKnowledge(options = {}) {
  const { force = false, category = null } = options;

  ensureKnowledgeDir();

  const results = {
    articles: [],
    errors: []
  };

  // Scan failure catalog
  console.log('[1/4] Scanning failure catalog', process.stdout.isTTY ? '.....' : '');
  const failureData = scanFailureCatalog();

  if (failureData.count === 0) {
    results.errors.push('No failure patterns found. Run sync first.');
  } else {
    // Categorize and generate articles
    const categories = categorizePatterns(failureData.patterns);

    console.log('[2/4] Scanning agent scores', process.stdout.isTTY ? '.......' : '');
    const agentData = scanAgentScores();

    console.log('[3/4] Scanning sync history', process.stdout.isTTY ? '........' : '');
    // Sync history is read but not actively used in this version
    const syncData = readDataFile('sync-status.json');

    console.log('[4/4] Generating knowledge articles', process.stdout.isTTY ? '....' : '');

    // Generate category articles
    const categoryKeys = Object.keys(categories);
    for (const cat of categoryKeys) {
      if (category && category !== cat) continue;

      const patterns = categories[cat];
      if (patterns.length === 0) continue;

      const article = generateArticle(cat, patterns);
      if (!article) continue;

      const filepath = path.join(KNOWLEDGE_DIR, article.name);
      if (shouldRegenerate(filepath, force)) {
        writeArticle(article);
        results.articles.push({
          name: article.name,
          path: filepath,
          patternCount: article.patternCount,
          status: 'generated'
        });
      }
    }

    // Generate agent performance article
    if (agentData.repoCount > 0 && (!category || category === 'agent')) {
      const agentArticle = generateAgentArticle(agentData);
      if (agentArticle) {
        const filepath = path.join(KNOWLEDGE_DIR, agentArticle.name);
        if (shouldRegenerate(filepath, force)) {
          writeArticle(agentArticle);
          results.articles.push({
            name: agentArticle.name,
            path: filepath,
            patternCount: agentArticle.patternCount,
            status: 'generated'
          });
        }
      }
    }
  }

  return results;
}

/**
 * CLI command handler
 */
function learnCommand(options = {}) {
  const force = options.force || false;
  const category = options.category || null;

  console.log('\nAnalyzing local data...\n');

  generateKnowledge({ force, category })
    .then(results => {
      if (results.articles.length === 0) {
        console.log('No new articles generated. Data may be stale or up-to-date.\n');
        if (results.errors.length > 0) {
          console.log('Issues:');
          results.errors.forEach(err => console.log(`  ⚠️  ${err}`));
        }
        console.log();
        return;
      }

      console.log('Generated:');
      results.articles.forEach(article => {
        console.log(`  ✅ ${article.name} (${article.patternCount} patterns → ${article.status})`);
      });

      console.log(`\nLocation: ${KNOWLEDGE_DIR}\n`);

      if (results.errors.length > 0) {
        console.log('Warnings:');
        results.errors.forEach(err => console.log(`  ⚠️  ${err}`));
        console.log();
      }
    })
    .catch(err => {
      console.error('Error generating knowledge articles:', err.message);
      process.exit(1);
    });
}

// Export
module.exports = {
  generateKnowledge,
  learnCommand,
  scanFailureCatalog,
  scanAgentScores,
  categorizePatterns,
  generateArticle,
  generateAgentArticle
};

// CLI entry point
if (require.main === module) {
  learnCommand(process.argv.slice(2));
}
